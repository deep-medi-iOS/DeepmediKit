import Foundation
import TensorFlowLite

public final class TFLiteModelRunner {
    public struct CoreOutput {
        public let ppg100: [Float]
        public let peakLogits: [Float]
        public let ppg30: [Float]
    }

    private let interpreter: Interpreter

    public init(
        modelName: String = "model_core",
        threadCount: Int = 2
    ) throws {
        guard let modelPath = Self.resolveModelPath(modelName: modelName) else {
            throw NSError(
                domain: "TFLiteModelRunner",
                code: -1,
                userInfo: [NSLocalizedDescriptionKey: "Could not find model: \(modelName).tflite"]
            )
        }

        var options = Interpreter.Options()
        options.threadCount = threadCount

        let interpreter = try Interpreter(modelPath: modelPath, options: options)
        try interpreter.allocateTensors()
        self.interpreter = interpreter
    }

    public func inputTensorByteCount(at index: Int = 0) throws -> Int {
        try interpreter.input(at: index).data.count
    }

    public func run(inputData: Data, inputIndex: Int = 0, outputIndex: Int = 0) throws -> Data {
        let expectedBytes = try inputTensorByteCount(at: inputIndex)
        guard inputData.count == expectedBytes else {
            throw NSError(
                domain: "TFLiteModelRunner",
                code: -2,
                userInfo: [NSLocalizedDescriptionKey: "Input byte count mismatch. expected=\(expectedBytes), got=\(inputData.count)"]
            )
        }

        try interpreter.copy(inputData, toInputAt: inputIndex)
        try interpreter.invoke()
        return try interpreter.output(at: outputIndex).data
    }

    public func runCore(frames: Data, timestamps: Data) throws -> CoreOutput {
        let runner = try interpreter.signatureRunner(with: "serving_default")
        try runner.resizeInput(named: "frames", toShape: [1, 451, 36, 36, 3])
        try runner.resizeInput(named: "timestamps", toShape: [1, 451])
        try runner.invoke(with: [
            "frames": frames,
            "timestamps": timestamps
        ])

        let ppg100Data = try runner.output(named: "ppg_100").data
        let peakLogitsData = try runner.output(named: "peak_logits").data
        let ppg30Data = try runner.output(named: "ppg_30").data

        return .init(
            ppg100: Self.floatArray(from: ppg100Data),
            peakLogits: Self.floatArray(from: peakLogitsData),
            ppg30: Self.floatArray(from: ppg30Data)
        )
    }

    private static func floatArray(from data: Data) -> [Float] {
        let count = data.count / MemoryLayout<Float>.stride
        return data.withUnsafeBytes { raw in
            guard let base = raw.baseAddress else { return [] }
            let ptr = base.assumingMemoryBound(to: Float.self)
            return Array(UnsafeBufferPointer(start: ptr, count: count))
        }
    }

    private static func resolveModelPath(modelName: String) -> String? {
        // 1) Resource bundle generated by CocoaPods: DeepmediKit.bundle
        if let bundleURL = Bundle.main.url(forResource: "DeepmediKit", withExtension: "bundle"),
           let bundle = Bundle(url: bundleURL),
           let path = bundle.path(forResource: modelName, ofType: "tflite") {
            return path
        }

        // 2) Framework bundle (works when used as framework target)
        let frameworkBundle = Bundle(for: TFLiteModelRunner.self)
        if let path = frameworkBundle.path(forResource: modelName, ofType: "tflite") {
            return path
        }

        // 3) App main bundle fallback
        return Bundle.main.path(forResource: modelName, ofType: "tflite")
    }
}

public struct PhysMorphNetResult: Equatable {
    public let sdnn: Double
    public let rmssd: Double
    public let hr: Double
    public let rrList: [Double]
    public let ppg: [Double]
    public let quality: Double
    
    public init(
        sdnn: Double,
        rmssd: Double,
        hr: Double,
        quality: Double,
        rrList: [Double],
        ppg: [Double]
    ) {
        self.sdnn = sdnn
        self.rmssd = rmssd
        self.hr = hr
        self.quality = quality
        self.rrList = rrList
        self.ppg = ppg
    }
}

